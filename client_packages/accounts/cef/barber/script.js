document.getElementById('masks').addEventListener('click', function() {
    var fut = document.getElementById('fut');
    var tol = document.getElementById('tol');
    var shtan = document.getElementById('shtan');
    var mas = document.getElementById('mas');
    var boot = document.getElementById('boot');
    if (mas.style.display === 'block') {
        mas.style.display = 'none';
    } else {
        mas.style.display = 'block';
        fut.style.display = 'none';
        tol.style.display = 'none';
        shtan.style.display = 'none';
        boot.style.display = 'none';
    }
});